package com.smartedu.demy.platform.iam.domain.model.valueobjects;

/**
 * Enumeration representing the possible roles assigned to a user account
 * within the system.
 */
public enum Roles {

    /**
     * Represents an administrative user with elevated privileges.
     */
    ADMIN,

    /**
     * Represents a teacher user with academic-related permissions.
     */
    TEACHER
}